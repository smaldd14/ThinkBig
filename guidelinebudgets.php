         <h2 class="tac"><i>Budget Guidelines for the Think BIG Grants</i></h2>
         <p class="tac"> Below are the budget guidelines for the preparation of proposals for Think BIG projects. Proposals not adhering to these guidelines will not be reviewed.</p>
         <ol>
   	<!--creates a numbered list of the guidelines for Budgts -->
            <li>Proposals for Think BIG projects must include a detailed budget worksheet listing expected expenditures (and, if pertinent, revenues, e.g., ticket sales for performances, fund raisers, etc.). Proposed budgets must use the budget worksheet included below. This budget worksheet should normally not exceed one or two pages.</li>
            <li>The budget worksheet should be divided into major categories (e.g., five to ten) outlining how the major expenditures (and revenues) will enhance the aims of the project. Most major categories should be subdivided into smaller budget items.</li>
            <li>When a budget item (or major category) includes multiple units of a single item, the budget should generally include the number of units to be purchased, the per unit price, and the total cost.</li>
            <li>The Think BIG committee will not provide additional funding for Think BIG projects beyond the funds initially requested. Please consider your estimates carefully (e.g., do not guess how much a piece of equipment will cost. Obtain price quotes from suppliers; the committee suggests a minimum of 2 quotes for expenses over $1,000).</li>
            <li>To help the Think BIG committee evaluate the different proposals, proposals for Think BIG projects must include a "budget narrative" or "budget notes" explaining the contents of its budget worksheet. The explanation for each major category and budget item should be sufficiently detailed to allow the Think BIG committee to relate funding requests to the goals of the proposal. The Think BIG committee will take into consideration both whether budget items help to accomplish the goals of the project and whether funding requests appear reasonable and realistic. This budget narrative or budget notes should normally not exceed one or two pages.</li>
            <li>The budget worksheet, budget narrative, or budget notes must include a time frame for the completion of the project.</li>
            <li>Projects expected to last more than six months are required to include both an overall budget for the project and a breakdown of expenditures (and revenues) for every six month period during the life of the project.</li>
            <li>Think BIG projects may not request funds for items that will result in reoccurring costs (e.g., maintenance costs for equipment).</li>
            <li>Think BIG projects may not request funds for items that fall within the regular expectations of instructors modifying their own course materials.</li>
            <li>Proposals may include a request for compensation. The committee suggests that faculty compensation be capped at a net of $2000 (plus current fringe, 10%). Compensation is subject to taxation. Requests for course releases and Fall or Spring semester overloads are discouraged but will be considered if approved in writing by the appropriate chair(s), dean(s), and the Provost.</li>
            <li>The committee encourages (but does not require) proposers to seek external funding. Any external funding must be included in the original budget proposal or submitted in a revised proposal.</li>
         </ol>
   
         <p> Below are some budget-related guidelines for recipients of Think BIG Grants </p>
   
         <ol>
   
   
            <li>Funds will not be transferred to the principal proposer of a Think BIG Grant upon its approval. Funds will be disbursed by HPU as costs are incurred and work is completed. All funding requests for preapproved budget items must be submitted in a timely fashion.</li>
            <li>To deviate substantially from their original budget, Think BIG project proposers must seek permission from the Think BIG committee through a written request. Deviations are deemed substantial if they exceed the smaller of $500 or 20 percent of a major expenditure category listed in your budget. Any written requests for consideration of budget deviations must indicate if a major category shortfall is accounted for by adjusting other categories, supplemental funding (departmental or external), etc. Note: The total award will not be increased due to deviations.</li>
            <li>Within one to two weeks of completion of the project, recipients of Think BIG Grants are required to turn in a brief budget-related report to the Think BIG committee summarizing the expenditures of their project, comparing expenditures to the original budget, and explaining any significant deviations from the original budget.</li>
            <li>Projects lasting more than six months are required to turn in brief budget-related reports to the Think BIG committee summarizing the expenditures of their project, comparing its expenditures to the original budget, and explaining any significant deviations from the original budget, every six months after approval of the project.</li>
            <li>Recipients of Think BIG Grants are required to maintain evidence of their expenditures (e.g., price quotes used in developing estimates in their budget and receipts for expenditures and revenues). The Think BIG committee may request this evidence from recipients of Think BIG Grants.</li>
         </ol>
   
