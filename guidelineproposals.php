         <h1> Think BIG!</h1>
         <h2 class="tac"><i>GUIDELINES FOR HPU Think BIG GRANTSEEKERS</i></h2>
         <p class="tac">The High Point University Think BIG Awards represent a one-time allocation of funds to be awarded on a competitive basis to exceptional faculty projects that enhance the student learning experience and enrich the life of the broader academic community. These grants aim to encourage faculty to imagine and design truly innovative, creative, and engaging approaches to student learning.</p>
         <h3>GRANTMAKING OBJECTIVES</h3>
         <p>The Think BIG Awards fund new or existing faculty initiatives that address emerging learning opportunities or needs. The major focus of these awards is to improve the effectiveness of faculty teaching and student learning by encouraging innovative and academically rigorous projects. We particularly seek out projects that make a significant impact on student experience. The objectives of Think Big Awards are to:</p>
         <ul>
            
            <li>Support creative and innovative teaching that measurably improves student learning;</li>
            <li>Support efforts that recognize and build on our academic programs and assets;</li>
            <li>Reach a broad segment of the community by making grants in many fields and for a wide range of purposes;</li>
            <li>Support projects that are germinal, seeding future projects and growth;</li>
            <li>Increase cooperation and coordination among academic program providers;</li>
            <li>Leverage additional program support from other private and public funding sources;</li>
            <li>Recognize and reward extraordinary teachers!</li>
         </ul>
         <h3>GRANTMAKING PRIORITIES</h3>
         <p>The Think BIG Awards Committee has identified these particular priorities and interests:</p>
         <p>STUDENT LEARNING ~ Creative approaches that improve student learning and enhance the academic environment. Applicants are asked to show how the project will extend beyond a single classroom, i.e., applicants are required to state how the project will be shared with and/or how the project impacts the broader community.</p>
   
         <p>ACADEMIC REPUTATION ~ Innovative programs and those that enhance the academic reputation of the University; projects that brand the academic program.</p>
   
         <p>STUDENT LEADERSHIP ~ Projects that build student academic and leadership skills, promote self-confidence, and encourage life-long learning.</p>
         <h3>GRANTMAKING RESTRICTIONS</h3>
         <p>The Think BIG Awards are not intended to fund the following: costs already incurred, ordinary capital requests, projects with recurring expenses.</p>
         <p>Only full-time faculty members are eligible to apply for a Think BIG Award. Individual faculty members may be the primary Faculty Proposer on only one project at a time; a primary Proposer may be a co-proposer on other projects.</p>
         <h3>HOW TO SUBMIT A GRANT REQUEST</h3>
         <p>Grantseekers must submit their proposal via an on-line application process. The Awards Committee will consider grant proposals on a competitive basis. This year, $100,000 are available for funding proposals.</p>
         <p>Proposal must be received no later than the announced date in February. This includes a copy of the signature page and an electronic copy of the proposal uploaded on this web site.</p>
         <p>We encourage grantseekers to submit proposals early and we will strictly enforce deadlines. Once a deadline passes, proposals will be reviewed during the next cycle, if funds are still available.</p>
